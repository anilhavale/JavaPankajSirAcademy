package DataTypes;

public class DownCasting 
{
	//   

	public static void main(String[] args) 
	{
		int  i =90;
		
		byte j=(byte)i;
		 
		System.out.println("j = "+ j);

		
		// long to int ,Downcasting it explicitly
		
		long x=20;
		int y=(int)x;
		
		System.out.println("long to int : y = "+ y);
	}

}
